    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if (txtid.getText().equals("") || txtus.getText().equals("") || txtpa.getText().equals("") || cat.getSelectedItem().equals("Type")) {
            JOptionPane.showMessageDialog(this, "Please the informations");
        } else {
            String[] colon = {"id_user", "username", "password", "type"};
            String[] inf = {txtid.getText(), txtus.getText(), txtpa.getText(), cat.getSelectedItem().toString()};
            String id = String.valueOf(user_table.getValueAt(user_table.getSelectedRow(), 0));
            System.out.println(db.queryUpdate("utilisateur", colon, inf, "id='" + id + "'"));
            table();
            actualiser();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String id = String.valueOf(user_table.getValueAt(user_table.getSelectedRow(), 0));
        if (JOptionPane.showConfirmDialog(this, "Do you really want to delete", "attention",
                JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            
            db.queryDelete("utilisateur", "id='" + id + "'");
        }
        
        table();
        

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (txtrech.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "please Insert what r you searching for");
        } else {
            if (catrech.getSelectedItem().equals("ID")) {
                rs = db.querySelectAll("utilisateur", "id_user LIKE '%" + txtrech.getText() + "%' ");
                user_table.setModel(new ResultSetTableModel(rs));
            } else if (catrech.getSelectedItem().equals("USERNAME")) {
                rs = db.querySelectAll("utilisateur", "username LIKE '%" + txtrech.getText() + "%' ");
                user_table.setModel(new ResultSetTableModel(rs));
                
            } else if (catrech.getSelectedItem().equals("TYPE")) {
                rs = db.querySelectAll("utilisateur", "type LIKE '%" + txtrech.getText() + "%' ");
                user_table.setModel(new ResultSetTableModel(rs));
                
            }
            
        }

    }//GEN-LAST:event_jButton6ActionPerformed

