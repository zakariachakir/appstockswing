    public boolean teststock() throws SQLException {
        boolean test;
        rs = db.querySelectAll("produit", "code_produit='" + txtcodeproduit.getText() + "'");
        while (rs.next()) {
            old = rs.getInt("stock");
        }
        rec = Integer.parseInt(txtstocksortie.getText().trim());
        if (old < rec) {
            test = false;
        } else {
            test = true;
        }
        return test;
    }

